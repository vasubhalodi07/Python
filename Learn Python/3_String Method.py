# Methods for Strings
# len : length
# find : find for poistion
# capitalize : first letter capital
# upper : all letter capital
# lower : all letter small
# isdigit : when string then op(False) when no then(True)
# isalpha : "Vasu Bhalodi" - False, "VasuBhalodi" - True
# count : char count
# replace : change the value of var
# * : print for multiple time
# center : center value for var (It is Use for Spaceing)

name = "Vasu"

print(len(name))
print(name.find("a"))
print(name.capitalize())
print(name.upper())
print(name.lower())
print(name.isdigit())
print(name.isalpha())
print(name.count("V"))
print(name.replace("Vasu", "Dhruv"))
print(name*3)
print(name.center(50))