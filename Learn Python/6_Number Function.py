# Number Function
# round - use for 3.14 then op 3, 3.59 then op 4
# math.ceil() - use for 3.14 to op is 4
# math.floor() - use for 3.14 to op is 3
# abs - absolute value like (-3) then 3
# pow - (value) ^ 2
# sqrt - square root
# max - find maximum value
# Functions of Number - sqrt, min, max, abs, pow, round, floor, ceil

import math

pi = 3.14
print(round(pi))
print(math.ceil(pi))
print(math.floor(pi))
print(abs(pi))
print(pow(pi,2))
print(math.sqrt(420))
print(max(10,20,30))
print(min(10,20,30))